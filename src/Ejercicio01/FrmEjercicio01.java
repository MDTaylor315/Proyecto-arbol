    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        int x = Integer.parseInt(txtValor.getText());
        abb.insertar(x);
        txtValor.setText("");
        txtValor.requestFocus();
        abb.preOrden(modelo);
        abb.enOrden(modelito);
        abb.postOrden(modelaso);
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int x = Integer.parseInt(JOptionPane.showInputDialog("Dato a Buscar: "));
        NodoArbol r = abb.buscar(x);

        if (r != null) {
            JOptionPane.showMessageDialog(null, "Dato se encuentra en el Arbol");
        } else {
            JOptionPane.showMessageDialog(null, "Dato no se encuentra en el Arbol");
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int x = Integer.parseInt(JOptionPane.showInputDialog("Dato a Eliminar: "));
        NodoArbol r = abb.buscar(x);

        if (r != null) {
            abb.eliminar(x);
            abb.preOrden(modelo);
            abb.enOrden(modelito);
            abb.postOrden(modelaso);
        }else{
            JOptionPane.showMessageDialog(null, "Dato no se encuentra en el Arbol");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

